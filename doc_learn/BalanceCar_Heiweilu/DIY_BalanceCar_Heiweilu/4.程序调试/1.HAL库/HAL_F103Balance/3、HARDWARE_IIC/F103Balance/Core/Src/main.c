void HAL_GPIO_EXTI_Callback(uint16_t GPIO_PIN)
{
	switch(GPIO_PIN)
	{
		case GPIO_PIN_5:
			LedFlash(50);
			MPU_Get_Gyroscope(&gyrox,&gyroy,&gyroz);								 //===得到陀螺仪数据
			MPU_DMP_Get_Data(&pitch,&roll,&yaw);										 //===得到欧拉角（姿态角）的数据
		//1.采集编码器数据&MPU6050角度信息。
			Encoder_Left=-Read_Speed(2);
			Encoder_Right=Read_Speed(3);  //编码器读出来的数据就是反馈回来的电机角度.
		//2.将数据压入闭环控制中，计算出控制输出量。
			Balance_Pwm =balance_UP(pitch,Mechanical_angle,gyroy);   //===直立环PID控制	
			Velocity_Pwm=velocity(Encoder_Left,Encoder_Right,Velocity);       //===速度环PID控制
			Turn_Pwm =turn(Encoder_Left,Encoder_Right,gyroz,Turn);        //===转向环PID控制
			Moto1=Balance_Pwm-Velocity_Pwm-Turn_Pwm;                 //===计算左轮电机最终PWM
			Moto2=Balance_Pwm-Velocity_Pwm+Turn_Pwm;                 //===计算右轮电机最终PWM
			Limit(&Moto1,&Moto2);  																					 //===PWM限幅
			Stop(pitch,12);																 //===检查角度以及电压是否正常
			Motor_Rotaton(Moto1,Moto2);                                    //===赋值给PWM寄存器
		//printf("%f,%f,%f\r\n", pitch, roll, yaw);   //串口打印欧拉角
			printf("%d,%d\r\n", Encoder_Left, Encoder_Right);   //串口打印编码器值
			break;
	}
}
